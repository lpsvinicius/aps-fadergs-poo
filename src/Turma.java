public abstract class Turma {

    protected double nota01;
    protected double nota02;
    protected double substitutiva;

    public double getNota01() {
        return nota01;
    }

    public void setNota01(double nota01) {
        this.nota01 = nota01;
    }

    public double getNota02() {
        return nota02;
    }

    public void setNota02(double nota02) {
        this.nota02 = nota02;
    }

    public double getSubstitutiva() {
        return substitutiva;
    }

    public void setSubstitutiva(double substitutiva) {
        this.substitutiva = substitutiva;
    }
}
